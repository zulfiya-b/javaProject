package arithmeticOperators;

public class ComparisonOperators {

    public static void main (String [] args){

        byte potato = 5;
        byte fries = 10;

        System.out.println(potato > fries);
        System.out.println(potato < fries);
        System.out.println(potato >= fries);
        System.out.println(potato <= fries);
        System.out.println(potato == fries);
        System.out.println(potato != fries);

        byte cherry = 2;
        byte berry = 5;
        short cookie = 7;
        short chips = 9;
        int place = 8;
        int seat = 6;
        long road = 10;
        long street = 12;

        System.out.println();



    }

}
