package Loops;

public class Practice {
    public static void main (String [] args){
        // nested loop - loop inside loop

        for (int i = 1; i <= 10; i++){
            for (int j = 1; j <= 10; j++){
                System.out.print(i);
                System.out.print(j);

            }
            System.out.println();
        }




    }




}

